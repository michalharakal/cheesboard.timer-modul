void ShowChr(uint8_t x, uint8_t y, int8_t ch, int8_t color)
{
    uint8_t i, j;
    
    if ((ch < 0x20) || (ch > 0x7b))   //  Convert to a printable character.
        ch = 92;    
    for ( i = 0; i < 8; i++ )     	    
      for ( j = 0; j < 5; j++ )            
    	  if ((FontLookup[ch - 32][j]) & (1 << i))
    	  {
    	  	gd_hw_set_pixel(x + j, y + (8 - i),  color);	
    	  }
}

void ShowStr(uint8_t x, uint8_t y, char *str, int8_t color)
{
	uint8_t i = 0;
	uint8_t j = y;
	while (*str) {
	  ShowChr(x, j, *str++, color);
	  x += 6;		  
	}	
}

void  gLine(int x1, int y1, int x2, int y2, uint8_t color)
{
  bresenham_init (x1, y1, x2, y2);
  int8_t MoveX, MoveY;
  gd_hw_set_pixel(x1, y1, color);	
  while (bresenham_next (&MoveX, &MoveY) != -1)  {    	  
  	  x1 = x1 + MoveX;
  	  y1 = y1 + MoveY;
  	  gd_hw_set_pixel(x1, y1, color);	  	
  }
}

